package ies.puerto;

import static java.lang.Math.PI;

public class Ejercicio2 {


    /**
     * Funcion que calcula el area de un circulo dado
     * @param radio del circulo a calcular
     * @return el area de un circulo
     */
    public double areaCirculo(int radio) {

        return (radio*radio)*PI;
    }
}